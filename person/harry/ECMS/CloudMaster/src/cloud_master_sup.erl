-author(kanghua ).  
-email(kanghua151@msn.com).  
-behaviour(supervisor).
%% --------------------------------------------------------------------
%% Include files
%% --------------------------------------------------------------------

%% --------------------------------------------------------------------
%% External exports
%% --------------------------------------------------------------------
-export([start_link/0]).

%% --------------------------------------------------------------------
%% Internal exports
%% --------------------------------------------------------------------
-export([init/1]).

%% --------------------------------------------------------------------
%% Macros
%% --------------------------------------------------------------------
-define(SERVER, ?MODULE).

%% ====================================================================
%% External functions
%% ====================================================================
start_link() ->
supervisor:start_link({local, ?SERVER}, ?MODULE, []).

%% ====================================================================
%% Server functions
%% ====================================================================
%% --------------------------------------------------------------------
%% Func: init/1
%% Returns: {ok,  {SupFlags,  [ChildSpec]}} |
%%          ignore                          |
%%          {error, Reason}
%% --------------------------------------------------------------------
init([]) -> 
    Meta_Server   = {meta_server,{meta_server,start_link,[]},
	      				permanent,2000,worker,[meta_server]},
    Status_Server = {status_monitor,{status_monitor,start_link,[]},
	     			 	permanent,2000,worker,[status_monitor]},
%%    {ok,{{one_for_all,0,1}, [AChild,BChild]}}.
	Servers = [Meta_Server,Status_Server],
    RestartStrategy = {one_for_one,0,1},
	{ok,{RestartStrategy, Servers}}.

